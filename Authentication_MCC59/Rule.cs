using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace Authentication_MCC59
{
    class Rule
    {
        public static string StringConstrain(string name)
        {
            if (name.Length >= 2)
            {
                return name;
            }
            else
            {
                return name;
            }
        }
    }
}
